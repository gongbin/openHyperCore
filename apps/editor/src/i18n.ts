/**
 * Minimal zero-dependency i18n. Chinese source strings are the keys, so the
 * zh locale is identity and untranslated keys degrade gracefully to Chinese.
 * Interpolation: t("已导入「{name}」", { name }).
 */
export type Lang = "zh" | "en";

const LS_KEY = "ohe.lang";

function detect(): Lang {
  try {
    const saved = localStorage.getItem(LS_KEY);
    if (saved === "zh" || saved === "en") return saved;
    return navigator.language?.toLowerCase().startsWith("zh") ? "zh" : "en";
  } catch {
    return "zh";
  }
}

let lang: Lang = detect();

export function getLang(): Lang { return lang; }

export function setLang(l: Lang): void {
  lang = l;
  try { localStorage.setItem(LS_KEY, l); } catch { /* private mode */ }
}

export function t(key: string, params?: Record<string, string | number>): string {
  let s = lang === "zh" ? key : EN[key] ?? key;
  if (params) for (const [k, v] of Object.entries(params)) s = s.split(`{${k}}`).join(String(v));
  return s;
}

